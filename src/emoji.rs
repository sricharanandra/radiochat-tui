/// Find emojis matching a partial shortcode
pub fn find_matching_emojis(partial: &str) -> Vec<(&'static str, &'static str, &'static str)> {
    let partial_lower = partial.to_lowercase();
    EMOJIS
        .iter()
        .filter(|(shortcode, _, _)| shortcode.starts_with(&partial_lower))
        .copied()
        .take(10) // Limit to 10 suggestions
        .collect()
}

/// Get emoji by exact shortcode
pub fn get_emoji(shortcode: &str) -> Option<&'static str> {
    let shortcode_lower = shortcode.to_lowercase();
    EMOJIS
        .iter()
        .find(|(sc, _, _)| *sc == shortcode_lower)
        .map(|(_, emoji, _)| *emoji)
}
